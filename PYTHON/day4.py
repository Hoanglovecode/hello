#Các hàm toán học phổ biến trong python
from math import*
#a=float(input('Căn bậc 2 số nhập từ bàn phím:'))
#sqrt:Căn
print(sqrt(64))
#isqrt:Căn lấy nguyên
#pow:Lũy thừa(mũ)
print(pow(2,10))
print(pow(27,1/3))
#ceil:ceiling(làm tròn lên trên)
print(ceil(2.8))#3
#floor(làm tròn xuống dưới)
print(floor(3.4))#3
#factorial(giai thừa)
print(factorial(10))#10!
#gcd(greatest common division):ước chung lớn nhất
print(gcd(100,455))#5
#comb(n,k):chỉnh hợp chọn k phần tử trong n phần tử(C)
print(comb(5,3))#10
#perm(n,k):tổ hợp chọn k phần tử trong n phần tử(A)
print(perm(5,3))#10*3!=60
#abs:Trị tuyệt đối
print(abs(-100))
print(round(234.4255245))#Làm tròn
#min max
print(min(1,3,4,5))
print(max(2,4,5,6))
#sum:tổng của list
print(sum([1,3,4,5]))
